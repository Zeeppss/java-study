package dev.zeeppss.recalling;

/*
Tipe data primitif = byte, short, int, long, float, double, boolean and char
Tipe data non primitif = String, Arrays, and Classes
*/

public class DataTypes {
    public static void main(String[] args) {
        byte myByte = 100;
        System.out.println("Byte: " + myByte); // Mencetak > 100

        short myShort = 1000;
        System.out.println("Short: " + myShort); // Mencetak > 1000

        int myInt = 10000;
        System.out.println("Int: " + myInt); // Mencetak > 10000

        long myLong = 100000L;
        System.out.println("Long: " + myLong); // Mencetak > 100000

        float myFloat = 1000000F;
        System.out.println("Float: " + myFloat); // Mencetak > 1000000.0

        double myDouble = 10.99d;
        System.out.println("Double: " + myDouble); // Mencetak > 10.99
    }
}
